void sensor_presence(){
    presence = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1);
    char buf[32];
        sprintf(buf, "Motion detection: %d\r\n", presence);
        HAL_UART_Transmit(&huart2, (uint8_t*)buf, strlen(buf), 1000);
}

void control_light(){
	char buf[32];
    if(presence){
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);
        sprintf(buf, "Light is ON\r\n");
    }else{
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_RESET);
        sprintf(buf, "Light is OFF\r\n");
    }
    HAL_UART_Transmit(&huart2, (uint8_t*)buf, strlen(buf), 1000);
}

void control_fan(){
    fuzzy_init(temp, hum, presence);
    pwm = crisp_output();
    char buf[32];
        sprintf(buf, "Fan value pwm: %d\r\n", pwm);
        HAL_UART_Transmit(&huart2, (uint8_t*)buf, strlen(buf), 1000);
}

void fan_service(){
	char buf[32];
    if(pwm >= 20){
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pwm); // duty
        sprintf(buf, "FAN is ON\r\n");
    }else{
        __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 0);
        sprintf(buf, "FAN is OFF\r\n");
    }
}

void oled_display(){
    ssd1306_Fill(Black);
    char buf[32];


       snprintf(buf, sizeof(buf), "Temp: %d C", temp);
       ssd1306_SetCursor(0, 0);
       ssd1306_WriteString(buf, Font_7x10, White);


       snprintf(buf, sizeof(buf), "Humi: %d %%", hum);
       ssd1306_SetCursor(0, 10);
       ssd1306_WriteString(buf, Font_7x10, White);


       snprintf(buf, sizeof(buf), "PWM: %d", pwm);
       ssd1306_SetCursor(0, 20);
       ssd1306_WriteString(buf, Font_7x10, White);


    ssd1306_UpdateScreen();
}

// debug bme280 detection
void BME280_CheckID(void)
{
    uint8_t chipID = 0;
    char buf[32];


    if (HAL_I2C_Mem_Read(&hi2c1, BME280_ADDRESS, ID_REG, 1, &chipID, 1, 1000) == HAL_OK)
    {
        sprintf(buf, "BME280 Chip ID = 0x%X\r\n", chipID);
        HAL_UART_Transmit(&huart2, (uint8_t*)buf, strlen(buf), 1000);
    }
    else
    {
        HAL_UART_Transmit(&huart2, (uint8_t*)"BME280 Read failed\r\n", 21, 1000);
    }
}
